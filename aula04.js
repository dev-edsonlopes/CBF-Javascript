//Operadores Matemáticos em Javascript

/*
    Adição                      +
    Subtração                   -        
    Multiplicação               *
    Divisão                     /

    resto ou modulo             %

    Incremento                  ++
    Decremento                  --

    Atribuição                  =
    Attr com adição             +=
    Attr com Subtração          -=
    Attre com Multiplicacão     *=
    Attr com Divisão            /=
*/

let num1 = 10
let num2 = 5
let res = 0

res = num1 + num2
console.log(num1+' + '+num2, ' = ', res)

res = num1 - num2
console.log(num1+' - '+num2, ' = ', res)

res = num1 * num2
console.log(num1+' * '+ num2, ' = ', res)

res = num1 / num2
console.log(num1+' / '+num2, ' = ', res)

